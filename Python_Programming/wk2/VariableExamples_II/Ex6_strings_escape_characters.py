# newline character \n, moves content to the next line
print('Hello\nWorld')

# tab character \t, adds a block of space
print("Hello\tPython\tWorld")
#-------------------

greet= "Hi"
language1 = "Python"
language2 = "C++"

# newline character combined with concatenation
print('\n')
print(greet+" "+language1)
print(greet+'\t'+language1)
print(greet+'\n'+language1+'\n'+language2+"\nI'm ready to code.")
#-------------------------
print('')
