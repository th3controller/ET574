# floats and basic maths
print(5.5+2.5)  # addition
print(5.5-2.5)  # subtraction
print(5.5*2.5)  # multiplication
print("")

print(5.5/2.5)  # floating-point division
print(5.5//2.5) # integer division (truncates the floating point information)
print(5.5%2.5)  # modulo division (returns the integer remainder of a division operation)
print("")

print(5.5**2.5) # exponentiation a**b is the same as a to the b power
print("")

# basic order of operations
print( 5.5+5.5/2.5 )      # division happens before addition
print( (5.5+5.5)/2.5 )    # addition happens before division
print("")
