# rules to define variable names

Class = "ET574"     #class is a reserved word
print(Class)        #Class is different from class, case sensitive

my_class = "ET574"    # names can use underscores _ instead of spaces
print(my_class)

_myclass = "ET574"     # names can start with underscores
print(_myclass)

myClass = "ET574"     # with multiple words, names can use proper case after the first word
print(myClass)

class574 = "ET574"    # names can use numbers if they are NOT the first letter
print(class574)
